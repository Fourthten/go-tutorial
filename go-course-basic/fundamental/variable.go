package main

import "fmt"

func main() {
	var name string

	name = "Ajung Setia"
	fmt.Println(name)

	name = "Ajung Agung"
	fmt.Println(name)

	var friendName = "Tia"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Ajung"
		lastName  = "Agung"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
